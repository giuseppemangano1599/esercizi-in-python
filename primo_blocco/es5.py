'''Scrivere un programma che calcola l'area di un rettangolo.
Chiedere all'utente la base e l'altezza. 
Stampare la risposta.'''

#chiedo di inserire la base e l'altezza
b = int(input("Inserisci la base: "))

h = int(input("Inserisci la altezza: "))

#L'area del rettangolo è base * altezza
print("L'area del rettangolo è ", b * h)